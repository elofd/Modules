def move(n, x, y):
    if n == 1:
        print('Переложить диск 1 со стержня номер {0} на стержень номер {1}'.format(x, y))
    else:
        move(n - 1, x, 6 - x - y)
        print('Переложить диск {0} со стержня номер {1} на стержень номер {2}'.format(n, x, y))
        move(n - 1, 6 - x - y, y)

n = int(input('Введите количество дисков: '))
move(n, 1, 3)

